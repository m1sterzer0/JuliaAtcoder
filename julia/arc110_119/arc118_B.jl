gss()::Vector{String} = split(gs())
gis()::Vector{Int64} = [parse(Int64,x) for x in gss()]
gfs()::Vector{Float64} = [parse(Float64,x) for x in gss()]

function solve(K::I,N::I,M::I,A::VI)
    B::VI = [a * M รท N for a in A]
    error = [(A[i] // N - B[i] // M,i) for i in 1:K]
    sort!(error,rev=true)
    numleft = M - sum(B)
    for i in 1:numleft
        B[error[i][2]] += 1
    end
    return join(B," ")
end

function main(infn="")
    global infile
    infile = (infn != "") ? open(infn,"r") : length(ARGS) > 0 ? open(ARGS[1],"r") : stdin
    K,N,M = gis()
    A::VI = gis()
    ans = solve(K,N,M,A)
    println(ans)
end

